class Date {
  int _year;
  int _month;
  int _day;
  int _hour;
  int _minute;
  int _second;

  int get year => _year;

  set year(int value) {
    _year = value;
  }

  int get month => _month;

  int get second => _second;

  set second(int value) {
    _second = value;
  }

  int get minute => _minute;

  set minute(int value) {
    _minute = value;
  }

  int get hour => _hour;

  set hour(int value) {
    _hour = value;
  }

  int get day => _day;

  set day(int value) {
    _day = value;
  }

  set month(int value) {
    _month = value;
  }
}
